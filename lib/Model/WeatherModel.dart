class WeatherModel {
  final String name;
  final String country;
  final String image;
  final String status;
  final double temp;
  final double MaxTemp;
  final double MinTemp;
  final double rain;
  WeatherModel({
    required this.name,
    required this.country,
    required this.image,
    required this.status,
    required this.temp,
    required this.MaxTemp,
    required this.MinTemp,
    required this.rain,
  });
}
